/**
 * TwinMe Insight Eval — Fixed Evaluation Harness
 * ================================================
 * DO NOT MODIFY THIS FILE.
 * This is the fixed evaluation harness for proactive insight quality.
 * The agent modifies insight-config.js only.
 *
 * Metric: insight_quality_score (0.0 – 1.0, HIGHER = better)
 *
 * Composite formula:
 *   insight_quality_score = 0.35 * specificity
 *                         + 0.25 * actionability
 *                         + 0.25 * personality_match
 *                         + 0.15 * timing_relevance
 *
 * Components (each 0.0 – 1.0):
 *   specificity       — Does it cite actual data points (numbers, names, dates)?
 *   actionability     — Can the user DO something concrete with this insight?
 *   personality_match — Does it sound like the user's twin (casual, personal)?
 *   timing_relevance  — Is it relevant to time of day / recent activity?
 *
 * Evaluation uses LLM-as-judge (DeepSeek via TIER_EXTRACTION for cost).
 *
 * Usage:
 *   node --env-file=.env twin-research/insight-eval.js
 */

