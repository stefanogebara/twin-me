/**
 * Cron: Morning Briefing
 * =======================
 * Runs daily at 10am UTC (7am São Paulo). For users with messaging channels,
 * generates and delivers the morning briefing directly. For all active users,
 * also sends an Inngest event (when Inngest is configured).
 *
 * Schedule: 0 10 * * * (daily at 10:00 UTC)
 * Security: protected by CRON_SECRET Bearer token.
 */

import express from 'express';
import { verifyCronSecret } from '../middleware/verifyCronSecret.js';
import { inngest, EVENTS } from '../services/inngestClient.js';
import { supabaseAdmin } from '../services/database.js';
import { logCronExecution, wasRecentlyRun } from '../services/cronLogger.js';
import { deliverInsight } from '../services/messageRouter.js';
import { complete, TIER_ANALYSIS } from '../services/llmGateway.js';
import { getBlocks } from '../services/coreMemoryService.js';
import { createLogger } from '../services/logger.js';

const log = createLogger('CronMorningBriefing');
const router = express.Router();

/**
 * Generate a plain-text morning briefing for WhatsApp/Telegram delivery.
 * Reads cached structured briefing from morning_briefing_cache if available,
 * otherwise falls back to a simple LLM-composed text.
 */
async function generateBriefingText(userId) {
  // Try to read the latest structured cache (generated by dashboard API)
  const cacheCutoff = new Date(Date.now() - 6 * 60 * 60 * 1000).toISOString();
  const { data: cached } = await supabaseAdmin
    .from('proactive_insights')
    .select('metadata')
    .eq('user_id', userId)
    .eq('category', 'morning_briefing_cache')
    .gte('created_at', cacheCutoff)
    .order('created_at', { ascending: false })
    .limit(1)
    .single();

  if (cached?.metadata?.briefing) {
    const b = cached.metadata.briefing;
    const parts = [b.greeting || 'Good morning'];
    if (b.schedule_summary) parts.push(b.schedule_summary);
    if (b.rest) parts.push(b.rest);
    if (b.music) parts.push(b.music);
    if (b.suggestion) parts.push(b.suggestion);
    return parts.filter(Boolean).join(' ');
  }

  // No cache — generate a minimal briefing from memory + core blocks
  try {
    const [coreBlocks, recentMemories] = await Promise.all([
      getBlocks(userId),
      supabaseAdmin
        .from('user_memories')
        .select('content')
        .eq('user_id', userId)
        .in('memory_type', ['platform_data', 'reflection'])
        .gte('created_at', new Date(Date.now() - 24 * 60 * 60 * 1000).toISOString())
        .order('importance_score', { ascending: false })
        .limit(5),
    ]);

    const soulSig = coreBlocks.soul_signature?.content || '';
    const memories = (recentMemories.data || []).map(m => m.content).join('\n');

    const { data: user } = await supabaseAdmin
      .from('users')
      .select('first_name')
      .eq('id', userId)
      .single();
    const firstName = user?.first_name || 'there';
    const hour = new Date().getUTCHours() - 3; // São Paulo
    const greeting = hour < 12 ? 'Good morning' : 'Good afternoon';

    const response = await complete({
      messages: [{
        role: 'user',
        content: `Write a 2-3 sentence morning briefing for ${firstName}. Warm, casual, like a smart friend. Use ONLY what's in the data below — don't invent details.

WHO THEY ARE: ${soulSig}
RECENT DATA: ${memories || 'No recent data'}

Respond with just the briefing text, no labels.`,
      }],
      tier: TIER_ANALYSIS,
      maxTokens: 200,
      temperature: 0.7,
      userId,
      serviceName: 'morning-briefing-cron',
    });

    const text = response?.content?.trim();
    return text ? `${greeting}, ${firstName}. ${text}` : `${greeting}, ${firstName}. Have a great day!`;
  } catch {
    return null;
  }
}

router.all('/', async (req, res) => {
  const startTime = Date.now();
  try {
    const authResult = verifyCronSecret(req);
    if (!authResult.authorized) {
      return res.status(authResult.status).json({ error: authResult.error });
    }

    if (await wasRecentlyRun('morning-briefing')) {
      return res.json({ success: true, triggered: 0, reason: 'cooldown' });
    }

    const weekAgo = new Date(Date.now() - 7 * 24 * 60 * 60 * 1000).toISOString();
    const { data: activeUsers } = await supabaseAdmin
      .rpc('get_active_user_ids', { since_timestamp: weekAgo });

    const uniqueUserIds = (activeUsers || []).map(u => u.user_id);

    if (uniqueUserIds.length === 0) {
      return res.json({ success: true, triggered: 0, reason: 'no_active_users' });
    }

    // Get users with messaging channels for direct delivery
    const { data: channelUsers } = await supabaseAdmin
      .from('messaging_channels')
      .select('user_id')
      .eq('is_enabled', true)
      .in('user_id', uniqueUserIds);

    const channelUserIds = new Set((channelUsers || []).map(u => u.user_id));

    let delivered = 0;
    let triggered = 0;
    let skipped = 0;

    for (const userId of uniqueUserIds) {
      // For users with messaging channels: generate and deliver directly (bypasses Inngest)
      if (channelUserIds.has(userId)) {
        try {
          // Per-user cooldown: skip if briefing already delivered today
          const cutoff = new Date(Date.now() - 20 * 60 * 60 * 1000).toISOString();
          const { count } = await supabaseAdmin
            .from('proactive_insights')
            .select('id', { count: 'exact', head: true })
            .eq('user_id', userId)
            .eq('category', 'briefing')
            .gte('created_at', cutoff);

          if ((count || 0) > 0) {
            skipped++;
          } else {
            const briefingText = await generateBriefingText(userId);
            if (briefingText) {
              const { data: insight } = await supabaseAdmin
                .from('proactive_insights')
                .insert({
                  user_id: userId,
                  insight: briefingText,
                  urgency: 'medium',
                  category: 'briefing',
                  delivered: false,
                })
                .select('id, insight, category, urgency')
                .single();

              if (insight) {
                await deliverInsight(userId, insight).catch(err =>
                  log.warn('Briefing channel delivery failed', { userId, error: err.message })
                );
                // Mark delivered so cron-deliver-insights doesn't double-send
                await supabaseAdmin
                  .from('proactive_insights')
                  .update({ delivered: true, delivered_at: new Date().toISOString() })
                  .eq('id', insight.id);
                delivered++;
                log.info('Morning briefing delivered directly', { userId });
              }
            }
          }
        } catch (err) {
          log.warn('Direct briefing delivery failed', { userId, error: err.message });
        }
      }

      // Also send Inngest event (no-op if Inngest not configured, useful when it is)
      try {
        await inngest.send({ name: EVENTS.GENERATE_BRIEFING, data: { userId } });
        triggered++;
      } catch (err) {
        log.warn('Failed to trigger Inngest morning briefing', { userId, error: err.message });
      }
    }

    const elapsed = Date.now() - startTime;
    log.info('Morning briefing cron complete', {
      activeUsers: uniqueUserIds.length, delivered, triggered, skipped, elapsedMs: elapsed,
    });

    await logCronExecution('morning-briefing', 'success', elapsed, {
      activeUsers: uniqueUserIds.length, delivered, triggered,
    });

    return res.json({ success: true, activeUsers: uniqueUserIds.length, delivered, triggered, elapsedMs: elapsed });
  } catch (err) {
    const elapsed = Date.now() - startTime;
    await logCronExecution('morning-briefing', 'error', elapsed, null, err.message);
    log.error('Morning briefing cron failed', { error: err.message });
    return res.status(500).json({
      success: false,
      error: process.env.NODE_ENV !== 'production' ? err.message : 'Internal cron error',
    });
  }
});

export default router;
