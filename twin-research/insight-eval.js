// ─── Config import (what changes between experiments) ────────────────────────
import {
  INSIGHT_PROMPT_TEMPLATE,
  INSIGHT_TEMPERATURE,
  INSIGHT_MAX_TOKENS,
  MIN_DATA_POINTS_REQUIRED,
  PERSONALITY_INJECTION_STRENGTH,
  MEMORIES_TO_SCAN,
  REFLECTIONS_TO_INCLUDE,
  DEDUP_THRESHOLD,
} from './insight-config.js';

