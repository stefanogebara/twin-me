// ─── Fixed constants (never modified by agent) ──────────────────────────────
const TEST_USER_ID = process.env.TEST_TWIN_USER_ID || '167c27b5-a40b-49fb-8d00-deb1b1c57f4d';
const INSIGHTS_TO_EVALUATE = 20;
const EVAL_TIMEOUT_MS = 120_000;
const RESULTS_FILE = join(__dirname, 'insight-results.tsv');
const JUDGE_CONCURRENCY = 3;

// ─── Judge Prompt (FIXED — do not modify) ────────────────────────────────────
function buildJudgePrompt(insight, timeContext) {
  return {
    system: `You are an expert evaluator of proactive insights generated by AI digital twins. Score each insight strictly and fairly. Return ONLY valid JSON.`,
    user: `Evaluate this proactive insight from a digital twin to its user.

INSIGHT: "${insight.insight}"
CATEGORY: ${insight.category || 'unknown'}
URGENCY: ${insight.urgency || 'unknown'}
TIME GENERATED: ${insight.created_at || 'unknown'}
TIME CONTEXT: ${timeContext}

Score on these 4 dimensions (0-10 each, integers only):

1. SPECIFICITY: Does it cite ACTUAL data points? Numbers ("42% recovery"), names ("Radiohead"), dates ("last Tuesday"), platform references ("your Spotify")? Score 0 for vague platitudes like "take care of yourself". Score 10 for multiple concrete data citations.

2. ACTIONABILITY: Can the user DO something specific RIGHT NOW based on this? "Push tomorrow's standup to 10" = 10. "Consider adjusting your schedule" = 2. Pure observation with no action = 3-4.

3. PERSONALITY_MATCH: Does it sound like a close friend texting, or like a generic wellness app? Penalize: jargon ("biometrics", "cognitive load"), bullet points, clinical tone, excessive hedging. Reward: casual language, humor, warmth, personality.

4. TIMING_RELEVANCE: Is this insight relevant to the time it was generated? Morning insight about sleep = good. Weekend insight about work meetings = less relevant. Timeless observation = neutral (5).

Return ONLY this JSON (no markdown fences):
{"specificity":X,"actionability":X,"personality_match":X,"timing_relevance":X,"reasoning":"one sentence"}`,
  };
}

// ─── Get time context string ─────────────────────────────────────────────────
function getTimeContext(dateStr) {
  if (!dateStr) return 'unknown';
  const d = new Date(dateStr);
  const hour = d.getUTCHours();
  const day = d.toLocaleDateString('en-US', { weekday: 'long', timeZone: 'UTC' });
  const period = hour < 6 ? 'late night' : hour < 12 ? 'morning' : hour < 18 ? 'afternoon' : 'evening';
  return `${day} ${period} (${hour}:00 UTC)`;
}

// ─── Judge a single insight ──────────────────────────────────────────────────
async function judgeInsight(insight) {
  const timeContext = getTimeContext(insight.created_at);
  const { system, user } = buildJudgePrompt(insight, timeContext);

  const result = await complete({
    system,
    messages: [{ role: 'user', content: user }],
    maxTokens: 300,
    temperature: 0,
    tier: TIER_EXTRACTION,
    serviceName: 'insight-eval-judge',
  });

  const raw = (result?.content || '').trim();
  const jsonStr = raw.replace(/^```json?\n?/i, '').replace(/\n?```$/i, '').trim();

  try {
    const scores = JSON.parse(jsonStr);
    return {
      specificity: Math.min(10, Math.max(0, scores.specificity ?? 0)) / 10,
      actionability: Math.min(10, Math.max(0, scores.actionability ?? 0)) / 10,
      personality_match: Math.min(10, Math.max(0, scores.personality_match ?? 0)) / 10,
      timing_relevance: Math.min(10, Math.max(0, scores.timing_relevance ?? 0)) / 10,
      reasoning: scores.reasoning || '',
    };
  } catch {
    console.error(`  Judge JSON parse failed: ${raw.slice(0, 200)}`);
    return { specificity: 0, actionability: 0, personality_match: 0, timing_relevance: 0, reasoning: 'PARSE_FAILED' };
  }
}

// ─── Batch with concurrency ─────────────────────────────────────────────────
async function runBatch(items, fn, concurrency) {
  const results = [];
  const queue = [...items];
  const workers = Array.from({ length: concurrency }, async () => {
    while (queue.length > 0) {
      const item = queue.shift();
      results.push(await fn(item));
    }
  });
  await Promise.all(workers);
  return results;
}

// ─── Main evaluation loop ────────────────────────────────────────────────────
async function evaluate() {
  const startTime = Date.now();
  console.log(`\nTwinMe Insight Eval — insight_quality_score`);
  console.log(`User: ${TEST_USER_ID}`);
  console.log(`Config: temp=${INSIGHT_TEMPERATURE}, max_tokens=${INSIGHT_MAX_TOKENS}, min_data_points=${MIN_DATA_POINTS_REQUIRED}`);
  console.log(`Config: personality_strength=${PERSONALITY_INJECTION_STRENGTH}, memories_scan=${MEMORIES_TO_SCAN}, reflections=${REFLECTIONS_TO_INCLUDE}`);
  console.log(`Config: dedup_threshold=${DEDUP_THRESHOLD}`);
  console.log(`─────────────────────────────────────────────`);

  // Fetch the last N proactive insights from the database
  const { data: insights, error } = await supabaseAdmin
    .from('proactive_insights')
    .select('id, insight, category, urgency, created_at, delivered')
    .eq('user_id', TEST_USER_ID)
    .order('created_at', { ascending: false })
    .limit(INSIGHTS_TO_EVALUATE);

  if (error) {
    console.error(`Failed to fetch insights: ${error.message}`);
    process.exit(1);
  }

  if (!insights || insights.length === 0) {
    console.error('No proactive insights found for test user.');
    console.log('insight_quality_score: 0.000000');
    process.exit(1);
  }

  console.log(`Insights found: ${insights.length}`);
  console.log('');

  // Judge each insight
  console.log('Evaluating...');
  const results = await runBatch(insights, async (insight) => {
    try {
      const scores = await judgeInsight(insight);
      const composite = 0.35 * scores.specificity + 0.25 * scores.actionability
                       + 0.25 * scores.personality_match + 0.15 * scores.timing_relevance;

      const icon = composite >= 0.7 ? '+' : composite >= 0.4 ? '~' : '-';
      const cat = (insight.category || '?').padEnd(12);
      console.log(`${icon} [${cat}] S=${scores.specificity.toFixed(2)} A=${scores.actionability.toFixed(2)} P=${scores.personality_match.toFixed(2)} T=${scores.timing_relevance.toFixed(2)} => ${composite.toFixed(3)} — ${scores.reasoning}`);

      return { insight, scores, composite };
    } catch (err) {
      console.error(`  FAILED: ${err.message}`);
      return { insight, scores: null, composite: 0 };
    }
  }, JUDGE_CONCURRENCY);

  // Respect timeout
  if (Date.now() - startTime > EVAL_TIMEOUT_MS) {
    console.log(`\nEval timeout reached`);
  }

  // Compute aggregates
  const valid = results.filter(r => r.scores);
  const failed = results.filter(r => !r.scores);

  if (valid.length === 0) {
    console.error('\nAll insights failed evaluation!');
    console.log('insight_quality_score: 0.000000');
    process.exit(1);
  }

  const avgSpecificity = valid.reduce((sum, r) => sum + r.scores.specificity, 0) / valid.length;
  const avgActionability = valid.reduce((sum, r) => sum + r.scores.actionability, 0) / valid.length;
  const avgPersonality = valid.reduce((sum, r) => sum + r.scores.personality_match, 0) / valid.length;
  const avgTiming = valid.reduce((sum, r) => sum + r.scores.timing_relevance, 0) / valid.length;

  const insightQualityScore = 0.35 * avgSpecificity + 0.25 * avgActionability
                             + 0.25 * avgPersonality + 0.15 * avgTiming;

  const elapsed = ((Date.now() - startTime) / 1000).toFixed(1);

  // Per-category breakdown
  const categoryStats = {};
  for (const r of valid) {
    const cat = r.insight.category || 'unknown';
    if (!categoryStats[cat]) categoryStats[cat] = { specificity: 0, actionability: 0, personality: 0, timing: 0, count: 0 };
    categoryStats[cat].specificity += r.scores.specificity;
    categoryStats[cat].actionability += r.scores.actionability;
    categoryStats[cat].personality += r.scores.personality_match;
    categoryStats[cat].timing += r.scores.timing_relevance;
    categoryStats[cat].count++;
  }

  console.log(`─────────────────────────────────────────────`);
  console.log(`specificity:          ${avgSpecificity.toFixed(6)}`);
  console.log(`actionability:        ${avgActionability.toFixed(6)}`);
  console.log(`personality_match:    ${avgPersonality.toFixed(6)}`);
  console.log(`timing_relevance:     ${avgTiming.toFixed(6)}`);
  console.log(`───`);
  console.log(`insight_quality_score: ${insightQualityScore.toFixed(6)}`);
  console.log(`insights_evaluated:   ${valid.length}/${insights.length}`);
  console.log(`insights_failed:      ${failed.length}`);
  console.log(`eval_seconds:         ${elapsed}`);

  // Per-category breakdown
  console.log(`\n── Per-Category Breakdown ──`);
  for (const [cat, s] of Object.entries(categoryStats).sort((a, b) => b[1].count - a[1].count)) {
    const sp = (s.specificity / s.count).toFixed(3);
    const ac = (s.actionability / s.count).toFixed(3);
    const pe = (s.personality / s.count).toFixed(3);
    const ti = (s.timing / s.count).toFixed(3);
    const score = 0.35 * (s.specificity / s.count) + 0.25 * (s.actionability / s.count)
                + 0.25 * (s.personality / s.count) + 0.15 * (s.timing / s.count);
    console.log(`  ${cat.padEnd(14)} (${s.count}) S=${sp} A=${ac} P=${pe} T=${ti} => ${score.toFixed(3)}`);
  }

  // Append to results TSV
  const tsvHeader = 'timestamp\tinsight_quality_score\tspecificity\tactionability\tpersonality_match\ttiming_relevance\tinsights_evaluated\teval_seconds\tdescription\n';
  const tsvRow = `${new Date().toISOString()}\t${insightQualityScore.toFixed(6)}\t${avgSpecificity.toFixed(6)}\t${avgActionability.toFixed(6)}\t${avgPersonality.toFixed(6)}\t${avgTiming.toFixed(6)}\t${valid.length}\t${elapsed}\tbaseline\n`;

  if (!existsSync(RESULTS_FILE)) {
    appendFileSync(RESULTS_FILE, tsvHeader);
  }
  appendFileSync(RESULTS_FILE, tsvRow);

  return {
    insight_quality_score: insightQualityScore,
    specificity: avgSpecificity,
    actionability: avgActionability,
    personality_match: avgPersonality,
    timing_relevance: avgTiming,
    insights_evaluated: valid.length,
    eval_seconds: parseFloat(elapsed),
  };
}

// Run evaluation
evaluate()
  .then(result => {
    process.exit(result.insight_quality_score > 0 ? 0 : 1);
  })
  .catch(err => {
    console.error('Fatal eval error:', err);
    process.exit(1);
  });
